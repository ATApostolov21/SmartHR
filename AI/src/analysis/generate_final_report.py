df = pd.DataFrame(data)
OUTPUT_DIR = 'models/final_report'

def generate_final_charts():
    # 1. Accuracy vs Recall Trade-off
    plt.figure(figsize=(10, 6))
    sns.scatterplot(data=df, x='Recall', y='Accuracy', hue='Type', style='Model', s=200, palette='viridis')
    
    # Annotate points
    for i in range(df.shape[0]):
        plt.text(df.Recall[i]+0.005, df.Accuracy[i]+0.002, df.Model[i], fontsize=9)
        
    plt.title('Trade-off: Accuracy vs Recall (Catching Leavers)', fontsize=14)
    plt.grid(True, linestyle='--', alpha=0.6)
    plt.savefig(f"{OUTPUT_DIR}/accuracy_vs_recall.png")
    
    # 2. Precision Comparison
    plt.figure(figsize=(10, 5))
    sns.barplot(data=df, x='Model', y='Precision', palette='magma')
    plt.title('Precision Comparison (Reducing False Alarms)', fontsize=14)
    plt.ylim(0, 0.8)
    plt.axhline(y=0.5, color='r', linestyle='--', label='50% Baseline')
    plt.legend()
    plt.savefig(f"{OUTPUT_DIR}/precision_comparison.png")

